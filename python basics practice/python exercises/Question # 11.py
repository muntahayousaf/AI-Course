# Question no 11: 
# Age in Months and Days Input your age in years. Calculate and print age in: Months , Days (approximate)

age_in_years = int(input("Enter age in years :"))

age_in_months = age_in_years * 12
print("Age in months =" , age_in_months , "months")

age_in_days = age_in_years *365
print("Age in days =" , age_in_days , "days")